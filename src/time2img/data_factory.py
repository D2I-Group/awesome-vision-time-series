def download_(root_path: str = './dataset'):
    import gdown
    import zipfile
    import shutil
    url = 'https://drive.google.com/uc?id=1alE33S1GmP5wACMXaLu50rDIoVzBM4ik'
    output = 'datasets.zip'
    gdown.download(url, output, quiet=False)
        
    with zipfile.ZipFile('datasets.zip', 'r') as zip_ref:
        zip_ref.extractall(root_path) 
    files = os.listdir(os.path.join(root_path, 'all_six_datasets/'))
    for f in files:
        if os.path.exists(os.path.join(root_path, f)):
            if os.path.isdir(os.path.join(root_path, f)):
                shutil.rmtree(os.path.join(root_path, f))
            else:
                os.remove(os.path.join(root_path, f))
        shutil.move(os.path.join(root_path, 'all_six_datasets', f), os.path.join(root_path, f))
    shutil.rmtree(os.path.join(root_path, 'all_six_datasets/'))
    os.remove(os.path.join('./', 'datasets.zip'))


