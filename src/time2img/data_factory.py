def data_provider(data: Literal["ETTh1", "ETTh2", "ETTm1", "ETTm2"], root_path: str, split: Literal["train", "val", "test"] = 'train',
                  timeenc: int = 0, size: list = None, features: Literal['S', 'M'] = 'S', download: bool = False) -> Dataset:
    if download:
        download_(root_path)

    Data = data_dict[data][0]
    data_path = data_dict[data][1]
    data_set = Data(
        root_path=root_path,
        data_path=data_path,
        flag=split,
        size=size,
        timeenc=timeenc,
        features=features
    )
    print(f"{data}_{split} Loaded, total series: {len(data_set)}")
    return data_set